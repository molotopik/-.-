void mission_6()
{
	cout << "6) Дано число A. Вычислить A^8 , используя вспомогательную переменную и три операции умножения. \n";
	int a, b;
	cout << "Введите A: ";
	cin >> a;
	b = a * a; // квадрат b
	b = b * b; // четвертая степень b
	a = b * b; // восьмая степень a
	cout << "Результат: " << a << endl;
	cout << "____________________________________________________________\n";
}

